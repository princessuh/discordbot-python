# cogs/help.py

from discord.ext import commands
import discord

class Help(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    @commands.command(name="도움말")
    async def help(self, ctx):
        help_text = """
**📌 명령어 안내**

__💬 기본 대화 기능__  
- `반가워` : 봇이 인사와 자기소개를 해요  
- `안녕` : 시간대에 따라 인사해요  
- `사랑해`  
- `고마워`  
- `심심해`  
- `몇 시야?`, `몇 시니?` `몇시야`, `몇 시야`, `몇 시니`, `몇 시인지 알아`, `지금 몇 시`, `현재 시간` : 현재 시간을 알려줘요  
- `기분`, `기분 어때`, `기분이 어때`, `기분어때` : 지금 기분이 어떤지 대답해요  
- `뭐해`, `뭐해?`, `뭐하니`, `뭐 하니`, `뭐 하는 중이야?` : 랜덤한 대답으로 반응해요 

__🗃️ 리스트 저장__  
- `저장` : 리스트 이름과 링크 혹은 설명을 입력해서 저장해요  
- `삭제` : 저장된 리스트를 삭제해요  
- `목록` : 저장된 리스트를 모두 보여줘요

__🎲 주사위 게임__  
- `주사위` : 포인트를 걸고 봇과 주사위 대결을 해요  
  (이기면 건 포인트의 2배 획득!)

__💰 포인트 기능__  
- `포인트` : 내 포인트를 확인해요  
- `포인트랭킹` : 서버 내 TOP3 랭킹을 보여줘요

__📅 출석 기능__  
- `출석` : 하루 한 번 출석하고 랜덤 포인트를 받아요  
- `출석통계` : 내 누적 출석 일수를 확인해요  
- `출석랭킹` : 서버 내 TOP3 출석 랭킹을 보여줘요

__📆 일정 관리__  
- `일정추가` : 알림 받을 일정을 등록해요  
- `일정삭제` : 등록된 일정을 삭제해요  
- `일정조회` : 일정 목록을 확인해요  
※ 10분 전 자동 DM 알림이 전송됩니다!  

__🚨 공지 등록__
- `공지등록` : 공지의 제목과 내용을 저장해요  
- `공지` : 등록된 공지를 보여줘요  

__🎟️ 환영 인사__  
- `환영인사` : 환영 인사를 보낼 채널을 지정해요
※ 지정하지 않을 시 환영 인사가 출력되지 않습니다!  

__⌛ 타이머__  
- `타이머` : 음성 채팅 이용 시간을 출력할 채널을 지정해요
※ 지정하지 않을 시 이용 시간이 출력되지 않습니다!
        """
        await ctx.send(help_text)

async def setup(bot):
    await bot.add_cog(Help(bot))
